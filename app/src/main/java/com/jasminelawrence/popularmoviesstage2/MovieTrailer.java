package com.jasminelawrence.popularmoviesstage2;

public class MovieTrailer {

    private String mName;
    private String mKey;

    public MovieTrailer(String name, String key){
        mName = name;
        mKey = key;

    }

    public String getmName() {
        return mName;
    }

    public String getmKey() {
        return mKey;
    }
}
